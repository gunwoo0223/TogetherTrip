package trip.user.dto;

public class InterestedFestivalDto {
	private String f_thumbnail;
	private String f_title;
	private String f_time;
	private String f_loc;
	private int f_num;
	private int g_num;
	
	public String getF_thumbnail() {
		return f_thumbnail;
	}
	public void setF_thumbnail(String f_thumbnail) {
		this.f_thumbnail = f_thumbnail;
	}
	public String getF_title() {
		return f_title;
	}
	public void setF_title(String f_title) {
		this.f_title = f_title;
	}
	public String getF_time() {
		return f_time;
	}
	public void setF_time(String f_time) {
		this.f_time = f_time;
	}
	public String getF_loc() {
		return f_loc;
	}
	public void setF_loc(String f_loc) {
		this.f_loc = f_loc;
	}
	public int getF_num() {
		return f_num;
	}
	public void setF_num(int f_num) {
		this.f_num = f_num;
	}
	public int getG_num() {
		return g_num;
	}
	public void setG_num(int g_num) {
		this.g_num = g_num;
	}
	
}
