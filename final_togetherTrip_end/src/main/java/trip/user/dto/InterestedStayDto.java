package trip.user.dto;

public class InterestedStayDto {
	private String s_thumbnail;
	private String s_title;
	private String s_loc;
	private String s_tel;
	private int s_num;
	private int g_num;
	
	public String getS_thumbnail() {
		return s_thumbnail;
	}
	public void setS_thumbnail(String s_thumbnail) {
		this.s_thumbnail = s_thumbnail;
	}
	public String getS_title() {
		return s_title;
	}
	public void setS_title(String s_title) {
		this.s_title = s_title;
	}
	public String getS_loc() {
		return s_loc;
	}
	public void setS_loc(String s_loc) {
		this.s_loc = s_loc;
	}
	public String getS_tel() {
		return s_tel;
	}
	public void setS_tel(String s_tel) {
		this.s_tel = s_tel;
	}
	public int getS_num() {
		return s_num;
	}
	public void setS_num(int s_num) {
		this.s_num = s_num;
	}
	public int getG_num() {
		return g_num;
	}
	public void setG_num(int g_num) {
		this.g_num = g_num;
	}
	
}
