package trip.user.dto;

public class InterestedRestaurantDto {
	private String r_thumbnail;
	private String r_title;
	private String r_menu;
	private String r_tel;
	private int r_num;
	private int g_num;
	
	public String getR_thumbnail() {
		return r_thumbnail;
	}
	public void setR_thumbnail(String r_thumbnail) {
		this.r_thumbnail = r_thumbnail;
	}
	public String getR_title() {
		return r_title;
	}
	public void setR_title(String r_title) {
		this.r_title = r_title;
	}
	public String getR_menu() {
		return r_menu;
	}
	public void setR_menu(String r_menu) {
		this.r_menu = r_menu;
	}
	public String getR_tel() {
		return r_tel;
	}
	public void setR_tel(String r_tel) {
		this.r_tel = r_tel;
	}
	public int getR_num() {
		return r_num;
	}
	public void setR_num(int r_num) {
		this.r_num = r_num;
	}
	public int getG_num() {
		return g_num;
	}
	public void setG_num(int g_num) {
		this.g_num = g_num;
	}
	
}
